n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1/ n2
print(f'{n1} + {n2} = {soma} ')
print(f'{n1} - {n2} = {subtracao}')
print(f'{n1} * {n2}= {multiplicacao}')
print(f'{n1} / {n2} = {divisao}' )